using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Coin : ObjectProjectile
{
    // Serialized Variables
    // Amount of damage the coin does
    [SerializeField]
    private int coinDamage = 13;

    PlayerAttack attack;

    // ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------ //
    // ------------------------------------------------------------------------------------------- GETTER/SETTER FUNCTIONS ---------------------------------------------------------------------------------------------- //
    // ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------ //

    // Set the amount of damage a coin does
    public void SetCoinDamage(int amount)
    {
        coinDamage += amount;
    }

    // ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------ //
    // ---------------------------------------------------------------------------------------------------  FUNCTIONS --------------------------------------------------------------------------------------------------- //
    // ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------ //

    private void Awake()
    {
        attack = FindObjectOfType<PlayerAttack>();
    }

    private void OnCollisionEnter2D(Collision2D collision)
    {
        // Destroy the coin object
        Destroy(gameObject);
        //Debug.Log("Collision Detected");

        EnemyHealth enemyHealthScript = collision.gameObject.GetComponent<EnemyHealth>();

        if (enemyHealthScript)
        {
            //Debug.Log("Collided with enemy!");
            // Deal damage to the enemy
            enemyHealthScript.ChangeHealth(-attack.GetCoinDamage());
        }
    }

    // ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------ //
    // ------------------------------------------------------------------------------------------------ END OF FUNCTIONS ------------------------------------------------------------------------------------------------ //
    // ------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------ //
}
